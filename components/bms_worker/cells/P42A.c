/**
 * @brief  Calculates the cell capacity in 0.0001Ah from a Voltage in 0.0001V
 *
 * @param v 0.0001V of a 1S1P Molicel P42A
 *
 * @retval 0.0001Ah of a 1S1P Molicel P42A @ v 
 */
uint32_t CELL_GetCapacityfromV(uint16_t v)
{
    return (((uint32_t)v) - 25000) / 17 * 42;
}

uint16_t CELL_GetVfromCapacity(uint32_t ah)
{
    return ah / 4.2 * 1.7 + 2.5;
}


