/**
 * @brief  Initializes the generic low-level firmware
 *
 * @retval Always true 
 */
bool HW_Init() 
{
    HAL_Init();
    return true;
}

void HW_DeInit()
{
    HAL_DeInit();
}

/**
 * @brief  Get the number of ticks since clock start
 *
 * @retval Number of ticks
 */
uint32_t HW_GetTick()
{
    return HAL_GetTick();
}


